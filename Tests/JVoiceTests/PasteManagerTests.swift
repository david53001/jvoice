import AppKit
import Foundation

final class MockPasteActionPerformer: PasteActionPerforming {
    let result: Bool
    private(set) var callCount = 0

    init(result: Bool) {
        self.result = result
    }

    func performPaste() -> Bool {
        callCount += 1
        return result
    }

    func performPaste(targetPID: pid_t) -> Bool {
        callCount += 1
        return result
    }
}

