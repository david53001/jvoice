@MainActor
@Test func failedPasteRestoresOriginalClipboard() async {
    let pasteboard = NSPasteboard.general
    pasteboard.clearContents()
    pasteboard.setString("ORIGINAL", forType: .string)

    let failingPerformer = MockPasteActionPerformer(result: false)
    let manager = PasteManager(performer: failingPerformer)
    let outcome = manager.paste("TRANSCRIPT", targetPID: 0)
    // AX-trusted in CI is unknowable: failingPerformer gives .targetRejected,
    // AX-denied short-circuits before clipboard capture.
    #expect(outcome == .targetRejected || outcome == .accessibilityDenied)

    // Wait past the 50ms restore delay + Task scheduling slack.
    try? await Task.sleep(nanoseconds: 600_000_000)

    let restored = pasteboard.string(forType: .string)
    #expect(restored == "ORIGINAL")
}

@MainActor
@Test func successfulPasteRestoresPriorClipboardAfterDelay() async {
    // Isolated named pasteboard: paste(_:targetPID:) captures/restores the
    // manager's injected pasteboard, so this is deterministic and side-effect-free.
    let pasteboard = NSPasteboard(name: NSPasteboard.Name("jvoice-test-\(UUID().uuidString)"))
    pasteboard.clearContents()
    pasteboard.setString("PRIOR", forType: .string)

    let performer = MockPasteActionPerformer(result: true)
    let manager = PasteManager(performer: performer, pasteboard: pasteboard,
                               accessibilityTrusted: { true })

    let outcome = manager.paste("TRANSCRIPT", targetPID: 0)
    #expect(outcome == .ok)
    #expect(performer.callCount == 1)
    // During the restore window the transcript is staged.
    #expect(pasteboard.string(forType: .string) == "TRANSCRIPT")

    // Wait past pasteRestoreDelay (0.30s) + Task scheduling slack.
    try? await Task.sleep(nanoseconds: 700_000_000)

    #expect(pasteboard.string(forType: .string) == "PRIOR")
}

@MainActor
@Test func secondPasteWithinRestoreWindowCoalescesRestore() async {
    // Two pastes inside the restore window: only the latest restore runs, and it
    // restores the clipboard captured before the *second* paste (the first
    // restore is cancelled, so the user's original is not clobbered back).
    let pasteboard = NSPasteboard(name: NSPasteboard.Name("jvoice-test-\(UUID().uuidString)"))
    pasteboard.clearContents()
    pasteboard.setString("FIRST", forType: .string)

    let performer = MockPasteActionPerformer(result: true)
    let manager = PasteManager(performer: performer, pasteboard: pasteboard,
                               accessibilityTrusted: { true })

    // First paste captures "FIRST" and stages "T1".
    #expect(manager.paste("T1", targetPID: 0) == .ok)
    #expect(pasteboard.string(forType: .string) == "T1")
    // Second paste (well within the 0.30s window) captures "T1" and stages "T2".
    #expect(manager.paste("T2", targetPID: 0) == .ok)
    #expect(pasteboard.string(forType: .string) == "T2")

    // Wait past pasteRestoreDelay + slack: only the second restore should run.
    try? await Task.sleep(nanoseconds: 700_000_000)

    // The first restore was cancelled (would have set "FIRST"); the surviving
    // restore writes the clipboard captured before the second paste ("T1").
    #expect(pasteboard.string(forType: .string) == "T1")
}

